CREATE TABLE EMPLEADOS
(
  id INTEGER(4) AUTO_INCREMENT,
  apellido VARCHAR(20) UNIQUE,
  cargo VARCHAR(9),
  jefe INTEGER(4),
  fecha_alta DATE,
  salario DOUBLE,
  comision DOUBLE,
  departamento INTEGER(4),
  FOREIGN KEY (departamento) REFERENCES DEPARTAMENTOS(id),
  PRIMARY KEY(id)
);



