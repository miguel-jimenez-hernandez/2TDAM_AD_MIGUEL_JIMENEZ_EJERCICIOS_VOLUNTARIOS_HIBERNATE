DROP TABLE IF EXISTS USUARIOS;

CREATE TABLE USUARIOS
       (id INTEGER(4) AUTO_INCREMENT,
        login VARCHAR(20) UNIQUE,
        pass VARCHAR(20),
        tipo VARCHAR(10),
        PRIMARY KEY(id));

